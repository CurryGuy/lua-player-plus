#include "UmdRip.h"

unsigned char *UMD::Buffer[BUFFER_SIZE] = { };

UMD::UMD() { }

const char *UMD::Init()
{
    int i;
    i = sceUmdCheckMedium();
    if(i == 0) {
        return("No Umd Inserted.\n");
    }
    i = sceUmdActivate(1, "disc0:");
    i = sceUmdWaitDriveStat(UMD_WAITFORINIT);
    return NULL;
}

const char *UMD::GetTitle() {
    SceUID fd;
    fd = sceIoOpen("disc0:/UMD_DATA.BIN", PSP_O_RDONLY, 0777);
    if(fd >= 0) {
        sceIoRead(fd, Title, 10);
        Title[10] = 0;
        sceIoClose(fd);
    } else { return("Cannot read the Umd Title.\n"); }
    return NULL;
}

bool UMD::size_is_valid(unsigned int blocks)
{
    char blockfile[128];
    SceUID fd, r;
    sprintf(blockfile, "disc0:/sce_lbn0x%x_size0x%x", blocks - 1, 1024 * 2);
    fd = sceIoOpen(blockfile, PSP_O_RDONLY, 0777);
    if(fd >= 0) {
        r = sceIoRead(fd, Buffer, 1024 * 2);
        sceIoClose(fd);
        return(r > 0);
    }
    return(false);
}

unsigned int UMD::GetSize() {
    unsigned int min, max, test;
    min = 0; max = 1024 * 1024;
    while(max > (min + 1)) {
        test = (max + min) / 2;
        if(size_is_valid(test))
           min = test;
        else
           max = test;
    }
    return(min - 1);
}

const char *UMD::DumpISO(char *IsoFile, unsigned int Blocks)
{
    char blockfile[128];
    SceUID fdin, fdout;
    int rd, wr;
    unsigned long int size, bread, bwritten;
    const char *Ret;
    sprintf(blockfile, "disc0:/sce_lbn0x%x_size0x%lu", 0, Blocks*2048L);
	fdin = sceIoOpen(blockfile, PSP_O_RDONLY, 0777);
	if(fdin < 0) {
	    return("Cannot read the UMD!\n");
	}
	fdout = sceIoOpen(IsoFile, PSP_O_WRONLY | PSP_O_CREAT | PSP_O_TRUNC, 0777);
	bread = bwritten = 0L;
	size = Blocks*2048L;
	while(bread < size) {
	rd = sceIoRead(fdin, Buffer, MIN(size, BUFFER_SIZE));
	bread += rd;
	wr = sceIoWrite(fdout, Buffer, rd);
	if(wr < 0) {
	    Ret = "Write Error!";
	    break;
	}
	bwritten += wr;
	}
	sceIoClose(fdin);
	sceIoClose(fdout);
	return Ret;
}
