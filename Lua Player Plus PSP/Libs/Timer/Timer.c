#include "Timer.h"

#include <stdlib.h>

#ifdef DEBUG
extern int dwrite_output(const char *format, ...);
#endif

static clock_t getCMs(void) {
    return clock() / (u64)(CLOCKS_PER_SEC / 1000);
}

LPP_Timer *LPP_TimerCreate(clock_t startTime)
{
    LPP_Timer *timer = (LPP_Timer *)malloc(sizeof(LPP_Timer));
	if(!timer) {
	    #ifdef DEBUG
		dwrite_output("Function %s Line %d : Cannot allocate 'timer' to memory.\n", __FUNCTION__, __LINE__);
		#endif
		return null;
	}

	timer->measuredTime = getCMs();
	timer->offset = startTime;

	return(timer);
}

clock_t LPP_TimerStart(LPP_Timer *timer)
{
    if(timer->measuredTime) {
        return(getCMs() - timer->measuredTime + timer->offset);
    } else {
        timer->measuredTime = getCMs();
        return(timer->offset);
    }
}

clock_t LPP_TimerGetTime(LPP_Timer *timer)
{
    if(timer->measuredTime) {
        return(getCMs() - timer->measuredTime + timer->offset);
    } else {
        return(timer->offset);
    }
}

clock_t LPP_TimerStop(LPP_Timer *timer)
{
    if(timer->measuredTime) {
        timer->offset = getCMs() - timer->measuredTime + timer->offset;
        timer->measuredTime = (0);
    }

    return(timer->offset);
}

clock_t LPP_TimerReset(LPP_Timer *timer, clock_t offset)
{
    clock_t ret = 0;
    if(timer->measuredTime) {
        ret = getCMs() - timer->measuredTime + timer->offset;
    } else {
        ret = timer->offset;
    }

    timer->offset = offset;
    timer->measuredTime = 0;
    return(ret);
}

void LPP_TimerFree(LPP_Timer *timer)
{
    if(timer) free(timer);
}
