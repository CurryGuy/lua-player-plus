#include "Battery.h"

extern u16 Read_eeprom(u8);
extern u32 Write_eeprom(u8, u16);

u16 LPP_BatteryGetTeoryCapacity(void) {
    return Read_eeprom(0x00);
}

u16 LPP_BatteryGetRealCapacity(void) {
    return Read_eeprom(0x30);
}

u16 LPP_BatteryGetCharge(void) {
    return Read_eeprom(0x11);
}

void LPP_BatteryWriteEeprom(u16 hi, u16 lo)
{
    Write_eeprom(0x09, lo);
    Write_eeprom(0x07, hi);
}

void LPP_BatteryPandorize(void)
{
    LPP_BatteryWriteEeprom(0xFFFF, 0xFFFF);
}

void LPP_BatteryNormalize(void)
{
    LPP_BatteryWriteEeprom(0x90CA, 0x0815);
}

u32 LPP_BatteryGetSerial(void)
{
    u16 hi = Read_eeprom(0x07);
    u16 lo = Read_eeprom(0x09);
    return (hi << 16) | lo;
}
